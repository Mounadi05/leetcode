char findTheDifference(char * s, char * t){
    int i = 1;
    
    unsigned int a = t[0];
    while(t[i])
    {
        a ^= t[i];
        i++;
    }
    i = 0;
    while(s[i])
    {
        a ^= s[i];
        i++;
    }
    return ((char)a);
}
