bool isPerfectSquare(int num){

    int i;
    
    if (num  <= 0)
        return false;
    if (num == 1)
        return true;
    if ((num % 2)  == 0)
        i = 2;
    if ((num % 2) != 0)
        i = 3;
    while ((i < 46341) && (i * i) <= num)
    {
        if ((i * i) == num)
            return true;
        i += 2;
    }
    return false;   
}
