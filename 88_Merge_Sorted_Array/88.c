void merge(int* nums1, int size1, int m, int* nums2, int size2, int n){
    int a = size1 - 1;
    m--;
    n--;
    while (m >= 0 && n >= 0 && a >= 0)
    {
        if (nums1[m] > nums2[n])
            nums1[a--] = nums1[m--];
        else
            nums1[a--] = nums2[n--];
    }
    while(m >= 0 && a >= 0)
        nums1[a--] = nums1[m--];
    while(n >= 0 && a >= 0)
        nums1[a--] = nums2[n--];
}

