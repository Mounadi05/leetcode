int findMaxConsecutiveOnes(int* nums, int numsSize){
    int a = 0;
    int i = 0;
    int j = 0;
    while (i < numsSize)
    {
        if (nums[i] == 1)
            a++;
        else
        {
            if (!j)
                j = a;
            if (j < a)
                j = a;
            a = 0;
        }
        i++;
    }
    if (j < a)
        j = a;
    return (j);
}
