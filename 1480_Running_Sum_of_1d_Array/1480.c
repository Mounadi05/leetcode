int* runningSum(int* arr, int size, int* rize){
    int i = -1;
    int a = 0;
    *rize = size;
    int *tab = malloc(4 * size);
    while (++i < size)
    {
        a = a + arr[i]; 
        tab[i] = a;
    }
    return (tab);
}
