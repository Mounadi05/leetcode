int removeElement(int* nums, int size, int val){
    int i = 0;
    int a = 0;
    while (i < size)
    {
        if (nums[i] == val && i + 1 < size) 
        {
            
            nums[i] = nums[i + 1];
        }
        else
            a++;
        i++;
    }
    return (a - 1);

}
