#include <string.h>
char * interpret(char * c){
    int i = 0;
    int j = 0;
    int a = strlen(c);
    char *str = malloc(a + 1);
    while (i < a )
    {
        if (c[i] != '(' && c[i] != ')')
            str[j++] = c[i];
        else if (c[i] == '(' && c[i + 1] == ')')
            str[j++] = 'o';
        i++;
    }
    str[j] = '\0';
    return (str);

}
