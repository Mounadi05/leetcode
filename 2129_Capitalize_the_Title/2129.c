#include <string.h>
char * capitalizeTitle(char * t){
    int i = -1;
    if (strlen(t) <= 2)
    {
        while (t[++i])
        if (t[i] >= 'A' && t[i] <= 'Z')
            t[i] +=32;
        return (t);
    }
    while (t[++i])
        if (t[i] >= 'A' && t[i] <= 'Z')
            t[i]+=32;
    i = 1;
    if (t[0] && t[1] && t[2] && t[1] != 32 && t[2] != 32)
        t[0] -= 32;
    while (t[i + 1])
    {
        if (t[i - 1] == ' ' && t[i + 2] && t[ i + 1] && t[i + 1] != 32 && t[i + 2] != 32)
            t[i] -=32;
        i++;
    }
    return (t);
}
