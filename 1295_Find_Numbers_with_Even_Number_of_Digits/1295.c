int even(int n)
{
    int i = 0;
    while (n && ++i)
        n = n / 10;
    return (i % 2);
}

int findNumbers(int* nums, int numsSize){
    int i = 0;
    int a = 0;
    while(i < numsSize)
        if(!even(nums[i++]))
            a++;
    return (a);

}
