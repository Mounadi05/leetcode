bool judgeSquareSum(int c){
        int a = 0;
        int b ;
    while (a < 46341 && a * a  <= c)
    {
        b = sqrt(c - (a*a));
        if (b * b == c - a*a)
            return true;
        a++;
    }
        return false;
}
